function loadChessboard(){
    for(i=7; i>=0; i--){
        for (j=0; j<=7; j+=1){
            var piece = chessboard[i][j];
            if (piece !== '0'){
                id = 7 - i
                row = document.getElementById(id.toString());
                squares = row.childNodes;
                squares[(j+1)*2-1].innerHTML = piece;
            }
        }
    }
}
